package CYDEO.mentoring.mentoring1;

public class Arrays {
    public static void main(String[] args) {
        // Non-primitive datatype: String (строка), Arrays (массив)
        String word = "Hello";
        //             01234

        int a = 12; // primitive datatype: int
        System.out.println(a);

        // Array
        int[] numbers = {12, 6, 3, 9}; // array of integers (массив чисел)
        //               0   1  2  3
        System.out.println(numbers[0]);
        System.out.println(numbers[2]);
        System.out.println(numbers[1]);



        // array of strings:
        String[] names = {"Abubakr", "Bekhruz", "Muhammadali", "Faridun"};
        //                    0           1           2            3
        System.out.println(names[1]);
        System.out.println(names[0]);


        double[] d = {1.6, 1.8, 1.9, 2.6, 6.7, 1.9, 1.0}; // array of doubles
        //             0    1    2    3





        //  How we create an array:

        //   Datatype[] variable = {value, value, value};
        //   array has indexes:       0      1      2
    }
}
