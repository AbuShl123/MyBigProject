package CYDEO.Days.day21_loops;

public class NestedLoop {
    public static void main(String[] args) {
        for (int i = 0; i < 4; i++) {
            for (int j = 0; j < 3; j++) {
                System.out.println("Hello world");
            }
            System.out.println("Hello Universe");
        }
    }
}
